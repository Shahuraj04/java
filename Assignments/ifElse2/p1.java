/*Write a program to check whether the given number is in the range of 1 to 1000 or
not.(take hardcoded values)*/
class p1{
	public static void main(String[] args){
		int x=-5;
		if(x>0 && x<=1000){
			System.out.println(x+" is in range 1 to 1000.");
		} else{ 
			System.out.println(x+" is not in range 1 to 1000");
		}
	
	
	
	}



}
